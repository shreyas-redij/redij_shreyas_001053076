package Business;

/**
 *
 * It contains saving information of the person
 */
public class SavingAccInfo {
   
    private String savingbankname;
    private String savingroutingnumber;
    private String savingaccountnumber;
    private String savingaccountbalance;
    private String savingaccounttype;

    public String getSavingbankname() {
        return savingbankname;
    }

    public void setSavingbankname(String savingbankname) {
        this.savingbankname = savingbankname;
    }

    public String getSavingroutingnumber() {
        return savingroutingnumber;
    }

    public void setSavingroutingnumber(String savingroutingnumber) {
        this.savingroutingnumber = savingroutingnumber;
    }

    public String getSavingaccountnumber() {
        return savingaccountnumber;
    }

    public void setSavingaccountnumber(String savingaccountnumber) {
        this.savingaccountnumber = savingaccountnumber;
    }

    public String getSavingaccountbalance() {
        return savingaccountbalance;
    }

    public void setSavingaccountbalance(String savingaccountbalance) {
        this.savingaccountbalance = savingaccountbalance;
    }

    public String getSavingaccounttype() {
        return savingaccounttype;
    }

    public void setSavingaccounttype(String savingaccounttype) {
        this.savingaccounttype = savingaccounttype;
    }
    
    
    
}
